print('Validate Relational Operations')
x=int(input('Enter first number: '))
y=int(input('Enter second number: '))
print('x =', x, 'y =', y)
print('x>y =', x > y)
print('x<y =', x < y)
print('x>=y =', x >= y)
print('x<=y =', x <= y)
print('x==y =', x == y)
print('x!=y =', x != y)

